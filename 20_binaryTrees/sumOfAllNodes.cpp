#include <bits/stdc++.h>
using namespace std;

struct Node
{
    int data;
    Node *left;
    Node *right;

    Node(int val)
    {
        data = val;
        left = NULL;
        right = NULL;
    }
};

void inorderTraversal(struct Node *root)
{
    if (root == NULL)
    {
        return;
    }

    inorderTraversal(root->left);
    cout << root->data << " ";
    inorderTraversal(root->right);
}

int sumOfAllNodes(Node *root)
{
    if (root == NULL)
    {
        return 0;
    }
    int sum = sumOfAllNodes(root->left) + sumOfAllNodes(root->right) + root->data;

    return sum;
}

